//Exercicios de fixação

//Operadores
//Soma
console.log(5 + 5);

//Multiplicação
console.log(5 * 5);

//Subtração
console.log(5 - 5);

//Divisão
console.log(5 / 5);

//Potencia
console.log(5 ** 5);

//Incremento
let value = 0;
value++;
console.log(value);

//Decremento
value--;
console.log(value);

//Comparações
//Verifica se são iguais
console.log(5 == 5);

//Verifica se são identicos
console.log("5" === 5);
console.log(5 === 5);

//verifica se são diferentes
console.log(5 != 5);
console.log(5 != 4);

